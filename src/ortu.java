    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
    int selectedRow = tdata.getSelectedRow();
    if (selectedRow != -1) {
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            // Ambil nilai NIK dari baris terpilih
            String nik = (String) tdata.getValueAt(selectedRow, 0);

            // Hapus baris terpilih dari tabel
            model.removeRow(selectedRow);

            // Hapus data dari database
            try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/ppdb_", "root", "");
                String query = "DELETE FROM orangtua WHERE NIK=?";
                PreparedStatement pst = cn.prepareStatement(query);
                pst.setString(1, nik);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error menghapus data dari database: " + ex.getMessage());
                Logger.getLogger(ortu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang akan dihapus");
    }
    }//GEN-LAST:event_bhapusActionPerformed

    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
         int selectedRow = tdata.getSelectedRow();
    if (selectedRow != -1) {
        String nik = (String) tdata.getValueAt(selectedRow, 0);

        // Ambil data dari inputan
        String nama = tnama.getText();
        String namaAyah = tnamaayah.getText();
        String pekerjaanAyah = tpekerjaanayah.getText();
        String gajiAyah = tgajiayah.getText();
        String namaIbu = tnamaibu.getText();
        String pekerjaanIbu = tpekerjaanibu.getText();
        String gajiIbu = tgajiibu.getText();

        // Update data di tabel GUI
        tdata.setValueAt(nik, selectedRow, 0);
        tdata.setValueAt(nama, selectedRow, 1);
        tdata.setValueAt(namaAyah, selectedRow, 2);
        tdata.setValueAt(pekerjaanAyah, selectedRow, 3);
        tdata.setValueAt(gajiAyah, selectedRow, 4);
        tdata.setValueAt(namaIbu, selectedRow, 5);
        tdata.setValueAt(pekerjaanIbu, selectedRow, 6);
        tdata.setValueAt(gajiIbu, selectedRow, 7);

        // Update data di database
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/ppdb_", "root", "");
            String query = "UPDATE orangtua SET nama=?, namaayah=?, pekerjaanayah=?, gajiayah=?, namaibu=?, pekerjaanibu=?, gajiibu=? WHERE nik=?";
            PreparedStatement pst = cn.prepareStatement(query);
            pst.setString(1, nama);
            pst.setString(2, namaAyah);
            pst.setString(3, pekerjaanAyah);
            pst.setString(4, gajiAyah);
            pst.setString(5, namaIbu);
            pst.setString(6, pekerjaanIbu);
            pst.setString(7, gajiIbu);
            pst.setString(8, nik);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error mengupdate data di database: " + ex.getMessage());
            Logger.getLogger(ortu.class.getName()).log(Level.SEVERE, null, ex);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang akan diupdate");
    }             
    }//GEN-LAST:event_beditActionPerformed

