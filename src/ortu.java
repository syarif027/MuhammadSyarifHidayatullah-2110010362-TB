    // Simpan data ke database
    try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/ppdb_", "root", "");
        String query = "INSERT INTO orangtua (nik, nama, namaayah, pekerjaanayah, gajiayah, namaibu, pekerjaanibu, gajiibu) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = cn.prepareStatement(query);
        pst.setString(1, nik);
        pst.setString(2, nama);
        pst.setString(3, namaAyah);
        pst.setString(4, pekerjaanAyah);
        pst.setString(5, gajiAyah);
        pst.setString(6, namaIbu);
        pst.setString(7, pekerjaanIbu);
        pst.setString(8, gajiIbu);
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error menyimpan data ke database: " + ex.getMessage());
        Logger.getLogger(ortu.class.getName()).log(Level.SEVERE, null, ex);
    }
